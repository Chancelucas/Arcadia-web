<h3>Gestion des Cookies sur le site "ARCADIA"</h3>
<br>
<p>La présente politique de gestion des cookies explique ce que sont les cookies, comment nous les utilisons sur le site "ARCADIA", les choix qui s'offrent à vous concernant leur utilisation, et comment vous pouvez les gérer.</p>
<br>
<ol>
  <li>
    <strong>Qu'est-ce qu'un cookie ?</strong><br>
    Un cookie est un petit fichier texte stocké sur votre ordinateur ou appareil mobile lorsque vous visitez un site web. Les cookies permettent à un site de reconnaître un utilisateur, de mémoriser ses préférences et de faciliter la navigation.
  </li>
  <br>
  <li>
    <strong>Types de cookies utilisés</strong><br>
    Le site "ARCADIA" utilise différents types de cookies pour les finalités suivantes :
    <ul>
      <li><strong>Cookies strictement nécessaires :</strong> Ces cookies sont indispensables au bon fonctionnement du site. Ils vous permettent de naviguer sur le site et d'utiliser ses fonctionnalités de base.</li>
      <li><strong>Cookies de performance :</strong> Ces cookies collectent des informations anonymes sur la manière dont les visiteurs utilisent le site. Ils permettent de mesurer l'audience et d'améliorer le site.</li>
      <li><strong>Cookies de fonctionnalité :</strong> Ces cookies permettent au site de mémoriser vos choix (comme votre nom d'utilisateur, votre langue ou la région où vous vous trouvez) pour vous offrir une expérience personnalisée.</li>
      <li><strong>Cookies de ciblage ou publicitaires :</strong> Ces cookies sont utilisés pour diffuser des publicités pertinentes en fonction de vos centres d'intérêt.</li>
    </ul>
  </li>
  <br>
  <li>
    <strong>Consentement à l'utilisation des cookies</strong><br>
    Lors de votre première visite sur le site "ARCADIA", un bandeau d'information apparaît pour vous informer de l'utilisation de cookies. Vous avez la possibilité d'accepter ou de refuser les cookies non essentiels. En continuant à naviguer sur le site sans modifier vos paramètres, vous consentez à l'utilisation de ces cookies.
  </li>
  <br>
  <li>
    <strong>Gestion des cookies</strong><br>
    Vous pouvez configurer votre navigateur pour refuser les cookies ou pour vous avertir lorsque des cookies sont envoyés. Cependant, si vous désactivez certains cookies, il se peut que certaines parties du site ne fonctionnent pas correctement. Voici comment gérer les cookies sur les navigateurs les plus courants :
    <ul>
      <li><a href="https://support.google.com/chrome/answer/95647?hl=fr" target="_blank">Google Chrome</a></li>
      <li><a href="https://support.mozilla.org/fr/kb/activer-desactiver-cookies-preferences" target="_blank">Mozilla Firefox</a></li>
      <li><a href="https://support.microsoft.com/fr-fr/help/278835/how-to-delete-cookie-files-in-internet-explorer" target="_blank">Internet Explorer</a></li>
      <li><a href="https://support.apple.com/fr-fr/HT201265" target="_blank">Safari</a></li>
      <li><a href="https://help.opera.com/fr/latest/web-preferences/#cookies" target="_blank">Opera</a></li>
    </ul>
  </li>
  <br>
  <li>
    <strong>Durée de conservation des cookies</strong><br>
    Les cookies sont conservés pour une durée maximale de 13 mois à compter de leur dépôt sur votre terminal. À l'expiration de cette durée, votre consentement sera à nouveau requis.
  </li>
  <br>
  <li>
    <strong>Modifications de la politique de gestion des cookies</strong><br>
    ARCADIA se réserve le droit de modifier la présente politique de gestion des cookies à tout moment. Toute modification prendra effet immédiatement dès sa publication sur le site. Nous vous encourageons à consulter régulièrement cette page pour être informé des éventuelles mises à jour.
  </li>
  <br>
  <li>
    <strong>Contact</strong><br>
    Si vous avez des questions concernant notre politique de gestion des cookies, vous pouvez nous contacter à l'adresse suivante : <a href="mailto:contact@arcadia.com">contact@arcadia.com</a>.
  </li>
</ol>